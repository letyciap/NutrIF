    <h2 style = "text-align: center; padding-top:50px;">O Que é o NutrIF?</h2>
    <p style = "text-align: center; padding:10px 200px; text-indent: 25px;">O projeto consiste na construção de uma ferramenta para gerenciamento de alimentos, cardápios e refeições (Lanches, almoço e jantar), que possa atender aos requisitos da direção acadêmica e do corpo estudantil do Instituto Federal de Educação, Ciência e Tecnologia do Rio Grande do Norte Campus Natal Zona Norte nos setores de alimentação e nutrição. O projeto visa auxiliar o sistema de refeições através de ferramentas que serão usadas por funcionários e alunos da instituição de ensino para acessibilizá-lo e democratizá-lo, desde a permissão da avaliação dos pratos preparados até o registro de dietas estritas ou intolerâncias alimentares.</p>
